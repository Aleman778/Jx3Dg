package jx3d.io.event;

/**
 * Window adapter is a convenience class that inherits window listener so you can choose to only override the
 * window listener callback functions you need.
 */
public class WindowAdapter implements WindowListener {

    /**
     * {@inheritDoc}
     */
    @Override
    public void windowResized(WindowEvent event) {

    }

    /**
     * {@inheritDoc}
     */
    @Override
    public void windowMoved(WindowEvent event) {

    }

    /**
     * {@inheritDoc}
     */
    @Override
    public void windowFocus(WindowEvent event) {

    }

    /**
     * {@inheritDoc}
     */
    @Override
    public void windowLostFocus(WindowEvent event) {

    }

    /**
     * {@inheritDoc}
     */
    @Override
    public void windowIconify(WindowEvent event) {

    }

    /**
     * {@inheritDoc}
     */
    @Override
    public void windowMaximize(WindowEvent event) {

    }

    /**
     * {@inheritDoc}
     */
    @Override
    public void windowClose(WindowEvent event) {

    }
}

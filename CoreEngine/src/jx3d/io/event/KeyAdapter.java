package jx3d.io.event;

/**
 * Key adapter is a convenience class that inherits key listener so you can choose to only override the
 * key listener callback functions you need.
 */
public class KeyAdapter implements KeyListener {

    /**
     * {@inheritDoc}
     */
    @Override
    public void keyDown(KeyEvent event) {

    }

    /**
     * {@inheritDoc}
     */
    @Override
    public void keyUp(KeyEvent event) {

    }
}

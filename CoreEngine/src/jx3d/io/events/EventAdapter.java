package jx3d.io.events;

/**
 * Event adapter is a convenience class that inherits event listener so you can choose to only override the
 * event listener callback functions you need.
 */
public class EventAdapter implements EventListener {

    /**
     * {@inheritDoc}
     */
    public void setup() {
    }

    /**
     * {@inheritDoc}
     */
    public void update() {
    }

    /**
     * {@inheritDoc}
     */
    public void draw() {
    }

    /**
     * {@inheritDoc}
     */
    public void keyDown(int key) {
    }

    /**
     * {@inheritDoc}
     */
    public void keyUp(int key) {
    }

    /**
     * {@inheritDoc}
     */
    public void keyRepeat(int key) {
    }

    /**
     * {@inheritDoc}
     */
    public void mousePressed(int button) {
    }

    /**
     * {@inheritDoc}
     */
    public void mouseReleased(int button) {
    }

    /**
     * {@inheritDoc}
     */
    public void mouseClicked(int button) {
    }

    /**
     * {@inheritDoc}
     */
    public void mouseEntered() {
    }

    /**
     * {@inheritDoc}
     */
    public void mouseExited() {
    }

    /**
     * {@inheritDoc}
     */
    public void mouseMoved(float dx, float dy) {
    }

    /**
     * {@inheritDoc}
     */
    public void mouseDragged(float dx, float dy) {
    }

    /**
     * {@inheritDoc}
     */
    public void mouseScrolled(float dx, float dy) {
    }

    /**
     * {@inheritDoc}
     */
    public void windowResized(int width, int height) {
    }

    /**
     * {@inheritDoc}
     */
    public void windowMoved(int x, int y) {
    }

    /**
     * {@inheritDoc}
     */
    public void windowFocus(boolean focused) {
    }

    /**
     * {@inheritDoc}
     */
    public void windowIconify(boolean iconified) {
    }

    /**
     * {@inheritDoc}
     */
    public void windowMaximize(boolean maximized) {
    }

    /**
     * {@inheritDoc}
     */
    public void windowClosed() {
    }
}
